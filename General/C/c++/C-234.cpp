#include <bits/stdc++.h>
using namespace std;
#define fst ios_base::sync_with_stdio(0); cin.tie(NULL); cout.tie(NULL);
const int mn = 1e5 + 10;
int nums[mn], n, ans = mn, p, g;
int main(){
    fst
    freopen("input.txt", "r", stdin);
	freopen("output.txt", "w", stdout);
    cin>>n;
    for(int i = 0; i < n; i++){
        cin>>nums[i];
        if(nums[i] <= 0) g++;
    }
    for(int i = 0; i < n - 1; i++){
        if(nums[i] <= 0) g--;
        if(nums[i] >= 0) p++;
        ans = ans > p + g ? p + g : ans;
    }
    cout<<ans;
    return 0;
}
