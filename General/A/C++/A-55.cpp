#include <bits/stdc++.h>
using namespace std;
bool nums[366];
int main(){
    int n, pos = 1, t; cin>>n;
    map <int, int> nums;
    for(int i = 1; i <= 2 * n; i++)
        t = i % n, pos += (pos + t > n ? (t - n) : t), nums[pos] = 1;
    for(int i = 1; i <= n; i++)
        if(!(nums[i])) return cout<<"NO", 0;
    return cout<<"YES", 0;
}
